				<h2>Plot</h2>
				<p>The backstory to Overwatch is described through animated shorts and other information distributed by Blizzard in promoting the game. Overwatch is set sixty years into the future of a fictionalized Earth, thirty years after the resolution of what is known as the "Omnic Crisis". Prior to the Omnic Crisis, humanity had been in a golden age of prosperity and technology development. Humans developed robots with artificial intelligence called "Omnics", which were put to use to achieve economic equality, and began to be treated as people in their own right. The Omnic Crisis began when the worldwide automated "omnium" facilities that produced them, started producing a series of lethal, hostile robots, that attacked humankind. The United Nations quickly formed Overwatch, an international task force to combat this threat and restore order.</p>
			</div>
			<div class="move">
				<p>Two veteran soldiers were put in charge of Overwatch: Gabriel Reyes and Jack Morrison. Though Overwatch successfully quelled the robotic uprising and brought a number of talented individuals to the forefront, a rift ended up developing between Reyes and Morrison, and Morrison became the leader of Overwatch while Reyes took charge of Blackwatch, Overwatch's covert operations division. Overwatch maintained peace across the world for several decades in what was called the "Overwatch Generation" as the team gained more members, but the rift between Morrison and Reyes intensified. Several allegations of wrongdoing and failures were leveled at Overwatch, leading to a public outcry against the organization and in-fighting between its members, prompting the UN to investigate the situation. During this, an explosion destroyed Overwatch's headquarters in Switzerland, purportedly killing Morrison and Reyes among others. The UN passed the Petras Act, which dismantled Overwatch and forbade any Overwatch-type activity.</p>
			</div>
			<div class="move">
				<p>Overwatch is set six years after the Petras Act; without Overwatch, corporations have started to take over, fighting and terrorism have broken out in parts of the globe, and there are signs of a second Omnic Crisis occurring in Russia. A former member of Overwatch, the intelligent gorilla Winston, decides to begin reforming Overwatch to protect the peace once again despite the Petras Act, with the team members recruiting old friends and gaining new allies in their fight. Thus it is also revealed that Reyes and Morrison were not killed in the explosion, that, in fact, was the result of a battle between them: Morrison became a masked vigilante known as "Soldier: 76", who is trying to uncover the reasons why Overwatch was shut down; while Reyes joined the criminal organisation Talon and became "Reaper", a terrorist disguised as Death.</p>
