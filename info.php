				<h2>Gameplay</h2>
				<p>Overwatch features a number of different game modes, principally designed around squad-based combat with two opposing teams of six players each. Players select one of over two dozen pre-made hero characters from one of three class types: Damage heroes that deal most of the damage to attack or defend control points, Tank heroes that can absorb a large amount of damage, and Support heroes that provide healing or other buffs for their teammates. Each hero has a unique skill kit, defining their intrinsic attributes like health points and running speed, their primary attacks, several active and passive skills, and an ultimate ability that can only be used after it has been charged through dealing damage. Players can change their hero during the course of a match, as a goal of Overwatch's design was to encourage dynamic team compositions that adapt to the situation. The game's genre has been described by some journalists as a "hero shooter", due to its design around specific heroes and classes.</p>
			</div>
			<div class="move">
				<p>The game features game modes for casual play, competitive ranked play, and for supporting esports competitions including Blizzard's Overwatch League. These modes generally are centered around sequentially securing control of points on the map, or escorting a payload between points on the map, with one team attacking while the other defends. Other modes set aside for casual matches include solo and team deathmatch, capture-the-flag, and unique modes run during various seasonal events. Regardless of winning or losing a match, players gain experience towards a player level, and on gaining a new level, receive loot boxes that contain cosmetic items that they can use to customize the appearance of the hero characters but otherwise does not affect gameplay. Loot boxes can also be purchased through microtransactions.</p>
