			<div>
				<h2>Overview</h2>
				<p>Overwatch is a team-based multiplayer first-person shooter developed and published by Blizzard Entertainment and released on May 24, 2016 for PlayStation 4, Xbox One, and Windows. Described as a "hero shooter", Overwatch assigns players into two teams of six, with each player selecting from a roster of 30 characters, known as "heroes", each with a unique style of play whose roles are divided into three general categories that fit their role. Players on a team work together to secure and defend control points on a map or escort a payload across the map in a limited amount of time. Players gain cosmetic rewards that do not affect gameplay, such as character skins and victory poses, as they play the game. The game was initially launched with casual play, with a competitive ranked mode, various 'arcade' game modes, and a player-customizable server browser subsequently included following its release. Additionally, Blizzard has added new characters, maps, and game modes post-release, all free of charge, with the only additional cost to players being optional loot boxes to earn cosmetic items.</p>
			</div>
			<div class="shift">
				<p>Overwatch is Blizzard's fourth major franchise and came about following the 2014 cancellation of the ambitious massively multiplayer online role-playing game Titan. A portion of the Titan team came up with the concept of Overwatch, based on the success of team-based first-person shooters like Team Fortress 2 and the growing popularity of multiplayer online battle arenas, creating a hero-based shooter that emphasized teamwork. Some elements of Overwatch borrow assets and concepts from the canceled Titan project. After establishing the narrative of an optimistic near-future Earth setting after a global crisis, the developers aimed to create a diverse cast of heroes that spanned genders and ethnicities as part of this setting. Significant time is spent adjusting the balance of the characters, making sure that new players would still be able to have fun while skilled players would present each other with a challenge.</p>
			</div>
			<div class="shift">
				<p>Overwatch was unveiled at BlizzCon 2014 in a fully playable state and was in a closed beta from late 2015 through early 2016. An open beta in May 2016 drew in nearly 10 million players. The release of the game was promoted with short animated videos to introduce the narrative and characters. Overwatch received universal acclaim from critics, who praised the game for its accessibility, diverse appeal of its hero characters, bright cartoonish art style, and enjoyable gameplay. Blizzard reported over US$1 billion in revenue during the first year of its release, and had more than 40 million players after two years. Overwatch is considered one of the greatest video games of all time, receiving numerous game of the year awards, alongside other accolades. The game has also become recognized as an esport, with Blizzard helping to fund and produce professional leagues, such as the Overwatch League.</p>
			</div>
